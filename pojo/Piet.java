package com.my.db;

public class Piet {
  private String barcode;
  private String merk;
  private String type;
  private String titel;
  private String prijs;
  private String prijs_per_d;
  private String spel_of_console;

  public String getBarcode() {
    return barcode;
  }

  public void setBarcode(String barcode) {
    this.barcode = barcode;
  }

  public String getMerk() {
    return merk;
  }

  public void setMerk(String merk) {
    this.merk = merk;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getTitel() {
    return titel;
  }

  public void setTitel(String titel) {
    this.titel = titel;
  }

  public String getPrijs() {
    return prijs;
  }

  public void setPrijs(String prijs) {
    this.prijs = prijs;
  }

  public String getPrijs_per_d() {
    return prijs_per_d;
  }

  public void setPrijs_per_d(String prijs_per_d) {
    this.prijs_per_d = prijs_per_d;
  }

  public String getSpel_of_console() {
    return spel_of_console;
  }

  public void setSpel_of_console(String spel_of_console) {
    this.spel_of_console = spel_of_console;
  }
}
